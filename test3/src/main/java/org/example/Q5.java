package org.example;

public class Q5 {

    public void integerFun(int num, int num2 ){

        int sum = num + num2;
        System.out.println("Sum: " + sum);

        int product = num * num2;
        System.out.println("Product: " + product);

        System.out.println("Concatenation: " + num + num2);

        double power = Math.pow(num, num2);
        System.out.println("Power: " + power);

    }
}
